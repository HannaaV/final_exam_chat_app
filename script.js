import {updateMembersDOM, addMessageToListDOM} from "/members.js"

const CLIENT_ID = '0skJPfW7PWVFcSWA';

export const drone = new ScaleDrone(CLIENT_ID, {
  data: {
    name: getRandomName(),
    color: getRandomColor(),
  },
});

let members = [];

drone.on('open', error => {
  if (error) {
    return console.error(error);
  }
  console.log('Welcome to ScaledroneChatApp!');

  const room = drone.subscribe('observable-room');
  room.on('open', error => {
    if (error) {
      return console.error(error);
    }
    console.log('You successfully joined room');
  });

  room.on('members', m => {
    members = m;
    updateMembersDOM(members);
  });

  room.on('member_join', member => {
    members.push(member);
    updateMembersDOM(members);
  });

  room.on('member_leave', ({id}) => {
    const index = members.findIndex(member => member.id === id);
    members.splice(index, 1);
    updateMembersDOM(members);
  });

  room.on('data', (text, member) => {
    if (member) {
      addMessageToListDOM(text, member);
    } else {
      
    }
  });
});

drone.on('close', event => {
  console.log('Connection was closed', event);
});

drone.on('error', error => {
  console.error(error);
});

